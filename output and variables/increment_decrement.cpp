#include <iostream>
using namespace std;
int main(){
    //increment  and decrement
    // this is a comment
    int x = 5;
    cout << x++ << endl; // post increment
    x--; // post decrement
    cout <<x << endl;
    ++x; //  pre increment --> x = x + 1
    cout << x << endl; 
    --x; // pre decrement --> x = x - 1;
    cout << x;
}